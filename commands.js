// • stat pathname – вивести інформацію про файл (дані дескриптору файлу).
// • ls – вивести список жорстких посилань на файли в CWD із зазначенням номерів
// дескрипторів файлів.
// • create pathname – створити новий звичайний файл та створити відповідне жорстке
// посилання на нього.
